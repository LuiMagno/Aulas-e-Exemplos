'''
Classes

Os nomes de classe devem ser substantivos, em maiúsculas e minúsculas com a primeira letra de cada palavra interna em maiúscula. Tente manter seus nomes de classe simples e descritivo. Sempre evite palavras-ligadas , evite todas siglas e abreviaturas, seja semântico.

Exemplo:

class Raster; 
class ImageSprite;
Interfaces

Nomes de interfaces devem ser usadas com as primeiras letras em maiúsculas como nome de classes.

Exemplo:

interface RasterDelegate;
interface Storing;
** Métodos (Methods)**

Métodos devem ser verbos, com a letra minúscula em primeiro lugar, com a primeira letra de cada palavra interna em maiúscula.

Exemplo:

run(); 
runFast(); 
getBackground();
Variáveis

Os nomes de variáveis não deve começar com underscore _ ou sinal de dólar $ personagens, mesmo que ambos não são permitidos. Os nomes de variáveis devem ser curtos, mas significativo. A escolha de um nome variável deve ser mnemônico, isto é, concebidos para indicar ao observador casual a intenção da sua utilização. Um personagem nomes de variáveis devem ser evitadas, exceto para temporários "descartáveis" variáveis. Os nomes comuns para variáveis temporárias são i, j, k, m, n e para inteiros, c, d, e e para caracteres

Exemplo:

int i; 
char c; 
float myWidth; 
Constantes

Os nomes de variáveis declaradas constantes de classes e de constantes ANSI deve ser todo em letras maiúsculas com palavras separadas por sublinhados ("_").

static final int MIN_WIDTH = 4; 
static final int MAX_WIDTH = 999;
static final int GET_THE_CPU = 1;
'''